def _font(run, size=14, bold=False):
    run.font.name = "Times New Roman"
    run.font.size = Pt(size)
    run.bold = bold
    run._element.rPr.rFonts.set(qn("w:eastAsia"), "Times New Roman")


def heading(doc, text, level=1):
    p = doc.add_paragraph()
    p.paragraph_format.space_before = Pt(6)
    p.paragraph_format.space_after = Pt(6)
    _font(p.add_run(text), bold=True)
    return p


def para(doc, text, indent=True, bullet=None):
    p = doc.add_paragraph()
    p.paragraph_format.line_spacing = 1.15
    p.paragraph_format.space_after = Pt(0)
    if indent:
        p.paragraph_format.first_line_indent = Cm(1.25)
    prefix = f"{bullet} " if bullet else ""
    _font(p.add_run(prefix + text))
    return p


def bullets(doc, items):
    for item in items:
        para(doc, item, indent=False, bullet="–")


def operation(doc, title, prep, steps, result, fig=None):
    heading(doc, title, level=2)
    if fig:
        p = doc.add_paragraph()
        p.paragraph_format.first_line_indent = Cm(1.25)
        _font(p.add_run(fig))
    para(doc, "Подготовительные действия:", indent=False)
    bullets(doc, prep if isinstance(prep, list) else [prep])
    para(doc, "Выполнение операции:", indent=False)
    for i, step in enumerate(steps, 1):
        para(doc, f"{i}. {step}", indent=False)
    para(doc, "Ожидаемый результат:", indent=False)
    bullets(doc, result if isinstance(result, list) else [result])
    doc.add_paragraph()


